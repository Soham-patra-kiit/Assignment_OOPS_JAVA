package com.Assignment2_Java;

class Hippo implements Animal
{
    public void makeNoise()
    {
        System.out.println("Hippo grunts.");
    }

    public void eat()
    {
        System.out.println("Hippo eats grass.");
    }

    public void sleep()
    {
        System.out.println("Hippo sleeps in water.");
    }

    public void roam()
    {
        System.out.println("Hippo roams near rivers.");
    }
}

